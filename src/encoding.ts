/**
 * Cria arquivo temporario .pr2 com conteudo em Windows-1252 (para compilacao).
 * Arquivo original permanece intacto.
 * Retorna path do temp ou undefined se nao precisou converter.
 */
export function createTempWin1252ForCompile(filePath: string): {
    success: boolean;
    pathToCompile: string;
    tempPath?: string;
    message?: string;
} {
    try {
        const buffer = fs.readFileSync(filePath);

        if (buffer.length === 0) {
            return { success: true, pathToCompile: filePath };
        }

        if (!isUtf8(buffer)) {
            // Ja esta em Windows-1252, compila o original
            return { success: true, pathToCompile: filePath };
        }

        // UTF-8: cria temp .pr2 no mesmo diretorio
        const parsed = path.parse(filePath);
        const tempPath = path.join(parsed.dir, parsed.name + '.pr2');

        const utf8Content = buffer.toString('utf8');
        const win1252Buffer = iconv.encode(utf8Content, 'win1252');
        fs.writeFileSync(tempPath, win1252Buffer, { flag: 'w' });

        return { success: true, pathToCompile: tempPath, tempPath };
    } catch (err) {
        const message = (err as Error).message;
        return { success: false, pathToCompile: filePath, message };
    }
}
